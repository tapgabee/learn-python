a = 7
b = 3

# การดำเนินการทางคณิตศาสตร์
print('a + b = ', a + b)
print('a - b = ', a - b)
print('a * b = ', a * b)
print('a / b = ', a / b)
print('a // b = ', a // b)  # ตัดเศษทิ้ง
print('a % b = ', a % b)  # หารเอาเศษ
print('a ^ b = ', a ** b)  # ยกกำลัง

# ลำดับการดำเนินการทางคณิตศาสตร์
# จาก [**, *, /] ไป [+, -]
# จาก ซ้าย ไป ขวา
